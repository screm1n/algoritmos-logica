// Criar duas expressões que resultem em TRUE (Aritméticos/Relacionais/Lógicos)
let verdadeira1 = 1 + 2 >= 3 && 3 > 2;
let verdadeira2 = 2 >= 1 + 1 || 3 > 5 ;

console.log(verdadeira1);
console.log(verdadeira2);

// Criar duas expressões que resultem em FALSE (Aritméticos/Relacionais/Lógicos)

let falso1 = 1 != 2 && 1 == 3 - 1;
let falso2 = 2 == 2 - 1 !== 3 > 3;

console.log(falso1);
console.log(falso2);